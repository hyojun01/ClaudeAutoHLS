// Read 256 samples from AXI-Stream and store in bit-reversed order
static void read_and_reorder(hls::stream<axis_pkt>& in, complex_t out[FFT_SIZE]) {
    complex_t temp[FFT_SIZE];

    // Read input stream — unpack 32-bit packed complex (real[31:16], imag[15:0])
    READ_LOOP:
    for (int i = 0; i < FFT_SIZE; i++) {
        #pragma HLS PIPELINE II=1
        axis_pkt pkt = in.read();
        data_t real_val, imag_val;
        real_val.range() = pkt.data.range(31, 16);
        imag_val.range() = pkt.data.range(15, 0);
        temp[i].real = real_val;
        temp[i].imag = imag_val;
    }

    // Apply bit-reversal reordering
    BIT_REV_LOOP:
    for (int i = 0; i < FFT_SIZE; i++) {
        #pragma HLS PIPELINE II=1
        out[bit_rev_table[i]] = temp[i];
    }
}

// Compute all 8 FFT butterfly stages
static void compute_fft(complex_t in[FFT_SIZE], complex_t out[FFT_SIZE]) {
    complex_t X[FFT_SIZE];

    // Copy input to working buffer
    COPY_IN:
    for (int i = 0; i < FFT_SIZE; i++) {
        #pragma HLS PIPELINE II=1
        X[i] = in[i];
    }

    // 8 butterfly stages
    STAGE_LOOP:
    for (int stage = 0; stage < FFT_STAGES; stage++) {
        int half = 1 << stage;           // Butterfly half-group size
        int step = 1 << (stage + 1);     // Full group size
        int tw_stride = HALF_FFT_SIZE >> stage; // Twiddle index stride

        BUTTERFLY_LOOP:
        for (int j = 0; j < HALF_FFT_SIZE; j++) {
            #pragma HLS PIPELINE II=1
            int group = j / half;
            int pos = j % half;
            int even_idx = group * step + pos;
            int odd_idx = even_idx + half;
            int tw_idx = pos * tw_stride;

            // Load twiddle factor
            tw_t wr = tw_real[tw_idx];
            tw_t wi = tw_imag[tw_idx];

            // Load odd element
            acc_t odd_r = X[odd_idx].real;
            acc_t odd_i = X[odd_idx].imag;

            // Complex multiply: W * X[odd] (natural-width multiply, promote on accumulate)
            acc_t prod_r = wr * odd_r - wi * odd_i;
            acc_t prod_i = wr * odd_i + wi * odd_r;

            // Load even element
            acc_t even_r = X[even_idx].real;
            acc_t even_i = X[even_idx].imag;

            // Butterfly: even +/- product
            X[even_idx].real = even_r + prod_r;
            X[even_idx].imag = even_i + prod_i;
            X[odd_idx].real  = even_r - prod_r;
            X[odd_idx].imag  = even_i - prod_i;
        }
    }

    // Copy result to output
    COPY_OUT:
    for (int i = 0; i < FFT_SIZE; i++) {
        #pragma HLS PIPELINE II=1
        out[i] = X[i];
    }
}

// Write 256 results to AXI-Stream output
static void write_output(complex_t in[FFT_SIZE], hls::stream<axis_pkt>& out) {
    WRITE_LOOP:
    for (int i = 0; i < FFT_SIZE; i++) {
        #pragma HLS PIPELINE II=1
        axis_pkt pkt;

        // Truncate acc_t back to data_t for output packing
        data_t real_out = (data_t)in[i].real;
        data_t imag_out = (data_t)in[i].imag;

        // Pack real[31:16] and imag[15:0] into 32-bit word
        ap_uint<32> packed;
        packed.range(31, 16) = real_out.range();
        packed.range(15, 0)  = imag_out.range();

        pkt.data = packed;
        pkt.keep = 0xF;
        pkt.strb = 0xF;
        pkt.last = (i == FFT_SIZE - 1) ? 1 : 0; // TLAST on last sample
        out.write(pkt);
    }
}

// ============================================================
// Top-Level Function
// ============================================================
void fft(
    hls::stream<axis_pkt>& in,
    hls::stream<axis_pkt>& out
) {
    // Interface pragmas
    #pragma HLS INTERFACE axis port=in
    #pragma HLS INTERFACE axis port=out
    #pragma HLS INTERFACE s_axilite port=return  // AXI-Lite control (start/done/idle)

    // Task-level pipelining between read, compute, and write stages
    #pragma HLS DATAFLOW

    complex_t buf_in[FFT_SIZE];
    complex_t buf_out[FFT_SIZE];

    read_and_reorder(in, buf_in);
    compute_fft(buf_in, buf_out);
    write_output(buf_out, out);
}
